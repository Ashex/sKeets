/* ── Public API ───────────────────────────────────────────────────── */

int font_draw_char(fb_t *fb, int x, int y, char c,
                   uint16_t fg, uint16_t bg) {
    if (c < FONT_FIRST || c > FONT_LAST) c = '?';
    const uint8_t *glyph = font_data[(unsigned char)c - FONT_FIRST];
    for (int row = 0; row < FONT_CHAR_H; row++) {
        uint8_t bits = glyph[row];
        for (int col = 0; col < FONT_CHAR_W; col++) {
            uint16_t color = (bits & (0x80 >> col)) ? fg : bg;
            fb_set_pixel(fb, x + col, y + row, color);
        }
    }
    return FONT_CHAR_W;
}

int font_draw_string(fb_t *fb, int x, int y, const char *s,
                     uint16_t fg, uint16_t bg) {
    if (!s) return x;
    int cx = x;
    while (*s) {
        cx += font_draw_char(fb, cx, y, *s, fg, bg);
        s++;
    }
    return cx;
}

int font_measure_string(const char *s) {
    if (!s) return 0;
    return (int)strlen(s) * FONT_CHAR_W;
}

int font_draw_wrapped(fb_t *fb, int x, int y, int max_w,
                      const char *s, uint16_t fg, uint16_t bg,
                      int line_spacing) {
    if (!s || max_w <= 0) return y;
    int chars_per_line = max_w / FONT_CHAR_W;
    if (chars_per_line <= 0) chars_per_line = 1;
    int cy = y;
    int len = (int)strlen(s);
    int i = 0;
    while (i < len) {
        /* Find the end of the current line (word-wrap) */
        int end = i + chars_per_line;
        if (end > len) end = len;
        /* Try to break at a space */
        if (end < len && s[end] != ' ' && s[end] != '\n') {
            int break_at = end;
            while (break_at > i && s[break_at] != ' ' && s[break_at] != '\n')
                break_at--;
            if (break_at > i) end = break_at;
        }
        /* Draw the segment */
        int cx = x;
        for (int k = i; k < end; k++) {
            if (s[k] == '\n') { end = k + 1; break; }
            cx += font_draw_char(fb, cx, cy, s[k], fg, bg);
        }
        /* Erase remainder of the line */
        if (cx < x + max_w)
            fb_fill_rect(fb, cx, cy, (x + max_w) - cx, FONT_CHAR_H, bg);
        cy += FONT_CHAR_H + line_spacing;
        /* Skip leading space on next line */
        i = end;
        if (i < len && s[i] == ' ') i++;
    }
    return cy;
}

int font_measure_wrapped(int max_w, const char *s, int line_spacing) {
    if (!s || max_w <= 0) return 0;
    int chars_per_line = max_w / FONT_CHAR_W;
    if (chars_per_line <= 0) chars_per_line = 1;
    int lines = 0;
    int len = (int)strlen(s);
    int i = 0;
    while (i < len) {
        int end = i + chars_per_line;
        if (end > len) end = len;
        if (end < len && s[end] != ' ' && s[end] != '\n') {
            int break_at = end;
            while (break_at > i && s[break_at] != ' ' && s[break_at] != '\n')
                break_at--;
            if (break_at > i) end = break_at;
        }
        for (int k = i; k < end; k++) {
            if (s[k] == '\n') { end = k + 1; break; }
        }
        lines++;
        i = end;
        if (i < len && s[i] == ' ') i++;
    }
    if (lines == 0) lines = 1;
    return lines * (FONT_CHAR_H + line_spacing);
}
